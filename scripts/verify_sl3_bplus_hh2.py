def _e2_e1_power(c):
    """Compute E2 * E1^c as a sum of PBW monomials {(c', e', d'): coeff}.
    E2 E1^0 = E2 = E1^0 E12^0 E2^1. So _e2_e1_power(0) = {(0, 0, 1): 1.0}.
    E2 E1 = q^{-1} E1 E2 - q^{-1} E12 = q^{-1} E1^1 E12^0 E2^1 - q^{-1} E1^0 E12^1 E2^0.
    E2 E1^c: use the recursion E2 E1^c = q^{-1} E1 (E2 E1^{c-1}) - q^{-1} q^{c-1} E1^{c-1} E12
                              = q^{-1} E1 (E2 E1^{c-1}) - q^{c-2} E1^{c-1} E12.
    """
    if c == 0:
        return {(0, 0, 1): 1.0}
    # Recurse
    prev = _e2_e1_power(c - 1)  # E2 E1^{c-1} in PBW form
    result = {}
    # Term 1: q^{-1} E1 * (E2 E1^{c-1})
    for (cp, ep, dp), v in prev.items():
        if cp + 1 < ELL:
            key = (cp + 1, ep, dp)
            result[key] = result.get(key, 0) + v * Q_INV
    # Term 2: -q^{c-2} E1^{c-1} E12
    # E1^{c-1} E12 is in PBW form (E1 first, then E12), so just (c-1, 1, 0).
    if c - 1 < ELL and 1 < ELL:
        key = (c - 1, 1, 0)
        phase = -Q ** (c - 2)
        result[key] = result.get(key, 0) + phase
    return {k: v for k, v in result.items() if abs(v) > 1e-13}


def e_multiply(left, right):
    """Multiply two E-parts (both in PBW form). left * right.
    For a monomial E1^{c2} E12^{e2} E2^{d2} in `right`, we compute
    left * (E1^{c2} E12^{e2} E2^{d2}) by applying the generators LEFT TO RIGHT:
    first E1^{c2} (leftmost), then E12^{e2}, then E2^{d2} (rightmost).
    i.e., ((left * E1^{c2}) * E12^{e2}) * E2^{d2}.
    """
    result = {}
    for (c2, e2, d2), v2 in right.items():
        acc = {mon: coeff for mon, coeff in left.items()}  # copy
        # Apply E1^{c2} on the right (leftmost generator of the right monomial)
        for _ in range(c2):
            acc = e_mult_by_E1_right(acc)
        # Apply E12^{e2} on the right
        for _ in range(e2):
            acc = e_mult_by_E12_right(acc)
        # Apply E2^{d2} on the right (rightmost generator)
        for _ in range(d2):
            acc = e_mult_by_E2_right(acc)
        # Add to result with coefficient v2
        for mon, coeff in acc.items():
            result[mon] = result.get(mon, 0) + coeff * v2
    return {k: v for k, v in result.items() if abs(v) > 1e-13}


