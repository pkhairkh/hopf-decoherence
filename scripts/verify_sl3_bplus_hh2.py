def e_mult_by_E1_left(elem):
    """Multiply E-part by E1 on the left: E1 * (E1^c E12^e E2^d) = E1^{c+1} E12^e E2^d.
    E1 commutes trivially with itself, and we're using PBW order E1^c E12^e E2^d,
    so E1 just adds to the c-exponent. Vanishes if c+1 == ell.
    """
    new = {}
    for (c, e, d), v in elem.items():
        if c + 1 < ELL:
            key = (c + 1, e, d)
            new[key] = new.get(key, 0) + v
    return {k: v for k, v in new.items() if abs(v) > 1e-13}


