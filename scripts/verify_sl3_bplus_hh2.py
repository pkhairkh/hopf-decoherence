def idx(a, b, c, e, d):
    """Index of K1^a K2^b E1^c E12^e E2^d (PBW order: E1, E12, E2)."""
    return a * (ELL**4) + b * (ELL**3) + c * (ELL**2) + e * ELL + d


def from_idx(i):
    return i // (ELL**4), (i // (ELL**3)) % ELL, (i // (ELL**2)) % ELL, (i // ELL) % ELL, i % ELL


def weight(i):
    """2D weight (alpha_1, alpha_2) components mod ell, using the root lattice.
    E1 has weight alpha_1 = (1, 0), E2 has weight alpha_2 = (0, 1), E12 has weight (1, 1).
    K1, K2 have weight (0, 0).
    So weight of K1^a K2^b E1^c E12^e E2^d = c*(1,0) + e*(1,1) + d*(0,1) = (c+e, e+d).
    """
    a, b, c, e, d = from_idx(i)
    w1 = (c + e) % ELL
    w2 = (e + d) % ELL
    return (w1, w2)


