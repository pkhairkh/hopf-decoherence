def verify_heat_kernel_H3(l=1.0):
    """Verify the heat kernel on H³ to machine precision.

    Compares the closed-form expression K(t) = (4πt)^{-3/2} e^{-t/l²}
    with the spectral integral ∫ σ²/(2π²) e^{-t(σ²+1)/l²} dσ.
    """
    t_values = [0.01, 0.05, 0.1, 0.5, 1.0, 2.0, 5.0, 10.0]
    results = []
    for t in t_values:
        K_closed = heat_kernel_H3_coincident(t, l=l)
        K_spectral = heat_kernel_H3_spectral(t, l=l)
        rel_err = abs(K_closed - K_spectral) / abs(K_closed) if abs(K_closed) > 1e-30 else 0
        results.append({
            "t": t, "closed": K_closed, "spectral": K_spectral,
            "rel_error": rel_err, "pass": rel_err < 1e-5,
        })
    return results


