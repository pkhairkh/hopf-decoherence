def heat_kernel_BTZ_image_sum(t, btz, n_max=20, Delta=2):
    """Heat kernel trace on H³/Γ (BTZ) via method of images — image terms only.

    The BTZ geometry is H³/Γ with Γ generated by a hyperbolic element
    with parameter α = 2πr₊/l. The n-th image has geodesic distance
    ρ_n = αn on H³.

    C_n(t) = 2 × β_H × (4πt)^{-3/2} × (αn/sinh(αn)) × exp(-(Δ-1)²t/l² - α²n²/(4t))

    Factor 2 for n and -n images (same contribution by symmetry).
    """
    l = btz.l
    beta_H = btz.beta_H
    alpha = btz.identification_parameter

    mass_factor = np.exp(-t * (Delta - 1)**2 / l**2)
    base = (1.0 / (4.0 * np.pi * t))**1.5

    C_images = 0.0
    for n in range(1, n_max + 1):
        rho_n = alpha * n
        if rho_n > 500:
            continue  # contribution negligible
        focus = rho_n / np.sinh(rho_n)
        C_n = beta_H * base * focus * np.exp(-rho_n**2 / (4.0 * t)) * mass_factor
        C_images += 2.0 * C_n
    return C_images


