def heat_kernel_H3_coincident(t, l=1.0):
    """Heat kernel on H³ at coincident points.

    K_{H³}(t; x, x) = (1/(4πt))^{3/2} × exp(-t/l²)

    The exp(-t/l²) is from the negative curvature of H³;
    for flat R³ this would be (4πt)^{-3/2} only.
    """
    return (1.0 / (4.0 * np.pi * t))**1.5 * np.exp(-t / l**2)


def heat_kernel_H3_spectral(t, l=1.0, s_max=50.0):
    """Heat kernel on H³ via spectral representation (Plancherel measure).

    K(t; x, x) = ∫₀^∞ dσ σ²/(2π²) × exp(-t(σ² + 1)/l²)

    Independent numerical check of the closed-form expression.
    The Plancherel measure μ(σ) = σ²/(2π²) encodes the eigenvalue
    density of the Laplacian on H³ with eigenvalues λ_σ = (σ² + 1)/l².
    """
    def integrand(sigma):
        mu = sigma**2 / (2.0 * np.pi**2)
        return mu * np.exp(-t * (sigma**2 + 1.0) / l**2)
    result, _ = integrate.quad(integrand, 0, s_max, limit=500)
    return result


def heat_kernel_H3_separated(t, rho, l=1.0):
    """Heat kernel on H³ at geodesic distance ρ.

    K_{H³}(t; ρ) = (4πt)^{-3/2} × (ρ/sinh(ρ/l)) × exp(-t/l² - ρ²/(4tl²))

    The factor ρ/sinh(ρ/l) is the focusing factor from negative curvature.
    """
    rho_l = rho / l
    if abs(rho_l) < 1e-10:
        return heat_kernel_H3_coincident(t, l)
    focus = rho_l / np.sinh(rho_l)
    return (1.0 / (4.0 * np.pi * t))**1.5 * focus * np.exp(
        -t / l**2 - rho**2 / (4.0 * t * l**2))


