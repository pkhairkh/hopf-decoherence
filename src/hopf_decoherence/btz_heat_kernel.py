class BTZGeometry:
    """Euclidean BTZ black hole geometry (non-rotating, r_- = 0).

    The Euclidean BTZ metric:
      ds² = l² [ (r² - r₊²)/r² dτ² + dr²/(r² - r₊²) + r² (dφ + r₊dτ/l)² ]

    This is a quotient H³/Γ where Γ ≅ Z is generated by a hyperbolic
    element of the isometry group of H³ with parameter α = 2πr₊/l.
    """

    def __init__(self, l, r_plus, G3=1.0):
        self.l = l
        self.r_plus = r_plus
        self.G3 = G3

    @property
    def beta_H(self):
        """Inverse Hawking temperature: β_H = 2πl²/r₊."""
        return 2.0 * np.pi * self.l**2 / self.r_plus

    @property
    def T_H(self):
        """Hawking temperature: T_H = r₊/(2πl²)."""
        return self.r_plus / (2.0 * np.pi * self.l**2)

    @property
    def S_BH(self):
        """Bekenstein-Hawking entropy: S_BH = πr₊/(2G₃) = A/(4G₃)."""
        return np.pi * self.r_plus / (2.0 * self.G3)

    @property
    def A_horizon(self):
        """Horizon area: A = 2πr₊."""
        return 2.0 * np.pi * self.r_plus

    @property
    def central_charge(self):
        """Brown-Henneaux central charge: c = 3l/(2G₃)."""
        return 3.0 * self.l / (2.0 * self.G3)

    @property
    def torus_modulus(self):
        """Torus modulus τ = i·l/r₊ for the BTZ boundary torus."""
        return 1j * self.l / self.r_plus

    @property
    def identification_parameter(self):
        """Parameter α = 2πr₊/l of the hyperbolic element in the BTZ quotient."""
        return 2.0 * np.pi * self.r_plus / self.l

    def summary(self):
        """Print a summary of the BTZ geometry."""
        print("=" * 65)
        print("  BTZ Black Hole Geometry Summary")
        print("=" * 65)
        print(f"  AdS radius l       = {self.l}")
        print(f"  Horizon radius r₊  = {self.r_plus}")
        print(f"  Newton constant G₃ = {self.G3}")
        print(f"  Hawking temp T_H   = {self.T_H:.6f}")
        print(f"  Inverse temp β_H   = {self.beta_H:.6f}")
        print(f"  BH entropy S_BH    = {self.S_BH:.6f}")
        print(f"  Horizon area A     = {self.A_horizon:.6f}")
        print(f"  Central charge c   = {self.central_charge:.6f}")
        print(f"  Torus modulus |τ|  = {abs(self.torus_modulus):.6f}")
        print(f"  Identification α   = {self.identification_parameter:.6f}")
