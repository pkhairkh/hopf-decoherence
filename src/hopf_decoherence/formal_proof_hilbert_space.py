def modified_qdim(j, r):
    r"""Modified quantum dimension d̃(P_j) for u_q(sl₂) at q = e^{2πi/r}.

    .. math::

        \tilde{d}(P_j) = \frac{(-1)^j \sin(\pi(j+1)/r)}{r \sin^2(\pi/r)}

    The Steinberg P(r-1) has d̃ = 0.

    Parameters
    ----------
    j : int
        Label of projective module P(j).
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    float
        Modified quantum dimension.
    """
    if j == r - 1:
        return 0.0
    return ((-1) ** j * np.sin(np.pi * (j + 1) / r)) / (r * np.sin(np.pi / r) ** 2)


def typical_modified_qdim(alpha, r):
    r"""Modified quantum dimension of typical module V_α.

    .. math::

        \tilde{d}(V_\alpha) = \frac{\sin(\pi\alpha/r)}{r \sin^2(\pi/r)}

    Parameters
    ----------
    alpha : float
        Continuous label.
    r : int
        Root of unity parameter.

    Returns
    -------
    float
        Modified quantum dimension.
    """
    return np.sin(np.pi * alpha / r) / (r * np.sin(np.pi / r) ** 2)


def modified_global_dimension(r):
    r"""Compute D̃² = Σ d̃(P_j)² + ∫ d̃(V_α)² dα.

    Parameters
    ----------
    r : int
        Root of unity parameter.

    Returns
    -------
    float
        Modified global dimension.
    """
    D2_disc = sum(modified_qdim(j, r) ** 2 for j in range(r - 1))

    sin2 = np.sin(np.pi / r) ** 2
    prefactor = 1.0 / (r * sin2) ** 2

    def integrand(alpha):
        return np.sin(np.pi * alpha / r) ** 2 * prefactor

    D2_cont = 0.0
    eps = 1e-6
    for k in range(r):
        a = k + eps
        b = k + 1 - eps
        val, _ = integrate.quad(integrand, a, b, limit=100)
        D2_cont += val

    return D2_disc + D2_cont


def bcgp_partition_discrete(beta, r):
    r"""BCGP discrete sector using modified trace.

    Z_BCGP_disc = Σ_j d̃(P_j) · e^{-βh_j}
    """
    Z = 0.0
    for j in range(r):
        d = modified_qdim(j, r)
        h = conformal_weight(j, r)
        Z += d * np.exp(-beta * h)
    return Z


def bcgp_partition_continuous(beta, r):
    r"""BCGP continuous sector using modified trace."""
    sin2 = np.sin(np.pi / r) ** 2
    prefactor = 1.0 / (r * sin2)

    def integrand(alpha):
        d = prefactor * np.sin(np.pi * alpha / r)
        h = typical_conformal_weight(alpha, r)
        return d * np.exp(-beta * h)

    Z = 0.0
    eps = 1e-6
    for k in range(r):
        a = k + eps
        b = k + 1 - eps
        val, _ = integrate.quad(integrand, a, b, limit=100)
        Z += val
    return Z


def bcgp_partition(beta, r):
    r"""BCGP partition function using modified trace.

    Z_BCGP = (1/D̃²) [Σ d̃(P_j) e^{-βh_j} + ∫ d̃(V_α) e^{-βh_α} dα]
    """
    D2 = modified_global_dimension(r)
    Z_disc = bcgp_partition_discrete(beta, r)
    Z_cont = bcgp_partition_continuous(beta, r)
    return (Z_disc + Z_cont) / D2


def verify_theorem4(r, beta=1.0):
    r"""Verify Theorem 4 (Modified Trace ≠ Hilbert Space Trace) for given r and β.

    Checks:
    (T4.1) d̃(P_j) ≠ dim P(j) for all j < r-1.
    (T4.2) d̃(P_j) < 0 for odd j (sign alternation).
    (T4.3) d̃(P_{r-1}) = 0 ≠ dim P(r-1) = r (Steinberg vanishing).
    (T4.4) Z_BCGP ≠ Z_full.
    (T4.5) The ratio d̃(P_j)/dim P(j) is NOT constant (not proportional).

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).
    beta : float
        Inverse temperature.

    Returns
    -------
    dict
        Verification results.
    """
    # (T4.1) d̃(P_j) ≠ dim P(j) for all j
    per_module = []
    ratios = []
    for j in range(r):
        d_mod = modified_qdim(j, r)
        dim_P = projective_module_dim(r, j)
        h_j = conformal_weight(j, r)

        differs = abs(d_mod - dim_P) > 1e-10
        is_negative = d_mod < 0

        ratio = d_mod / dim_P if dim_P > 0 else float('inf')
        if j < r - 1:
            ratios.append(ratio)

        per_module.append({
            'j': j,
            'dim_P': dim_P,
            'd_tilde': d_mod,
            'differs': differs,
            'is_negative': is_negative,
            'ratio_d_tilde_over_dim': ratio,
            'h_j': h_j,
        })

    all_differ = all(m['differs'] for m in per_module)

    # (T4.2) Sign alternation
    negative_j = [m['j'] for m in per_module if m['is_negative']]

    # (T4.3) Steinberg vanishing
    steinberg_d_tilde = modified_qdim(r - 1, r)
    steinberg_vanishes = abs(steinberg_d_tilde) < 1e-15

    # (T4.4) Z_BCGP ≠ Z_full
    Z_full_val = full_thermal_trace(beta, r)
    Z_bcgp_val = bcgp_partition(beta, r)
    Z_differ = abs(Z_full_val - Z_bcgp_val) > 1e-8

    # (T4.5) Ratios not constant
    if len(ratios) >= 2:
        ratio_range = max(ratios) - min(ratios)
        ratios_not_constant = ratio_range > 1e-10
    else:
        ratios_not_constant = True

    return {
        'r': r,
        'beta': beta,
        'all_d_tilde_differ_from_dim': all_differ,
        'negative_sectors': negative_j,
        'steinberg_d_tilde_zero': steinberg_vanishes,
        'Z_full': Z_full_val,
        'Z_bcgp': Z_bcgp_val,
        'Z_differ': Z_differ,
        'ratios_not_constant': ratios_not_constant,
        'per_module': per_module,
        'theorem4_holds': all_differ and steinberg_vanishes and Z_differ and ratios_not_constant,
    }


