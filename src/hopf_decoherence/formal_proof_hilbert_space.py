# 4. THEOREM 3: FULL TRACE = HILBERT SPACE TRACE
# ============================================================================

def full_thermal_trace_discrete(beta, r):
    r"""Compute the discrete sector of the full thermal trace.

    By Theorem 3:
        Tr_H(e^{-βH})_disc = Σ_{j=0}^{r-1} dim P(j) · e^{-βh_j}

    Using Theorem 2:
        = Σ_{j=0}^{r-2} 2r · e^{-βh_j} + r · e^{-βh_{r-1}}

    Parameters
    ----------
    beta : float
        Inverse temperature.
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    float
        Discrete sector thermal trace.
    """
    Z = 0.0
    for j in range(r):
        h_j = conformal_weight(j, r)
        dim_P = projective_module_dim(r, j)
        Z += dim_P * np.exp(-beta * h_j)
    return Z


def full_thermal_trace_continuous(beta, r):
    r"""Compute the continuous sector of the full thermal trace.

    Tr_H(e^{-βH})_cont = ∫_0^r dim(V_α) · e^{-βh_α} dα = ∫_0^r r · e^{-βh_α} dα

    Parameters
    ----------
    beta : float
        Inverse temperature.
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    float
        Continuous sector thermal trace.
    """
    def integrand(alpha):
        h = typical_conformal_weight(alpha, r)
        return r * np.exp(-beta * h)

    Z = 0.0
    eps = 1e-6
    for k in range(r):
        a = k + eps
        b = k + 1 - eps
        val, _ = integrate.quad(integrand, a, b, limit=100)
        Z += val
    return Z


def full_thermal_trace(beta, r):
    r"""Compute the full thermal trace Tr_H(e^{-βH}).

    By Theorem 3:
        Tr_H(e^{-βH}) = Z_full(r, β)

    Parameters
    ----------
    beta : float
        Inverse temperature.
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    float
        Full thermal trace = Z_full.
    """
    return (full_thermal_trace_discrete(beta, r)
            + full_thermal_trace_continuous(beta, r))


def verify_theorem3(r, beta=1.0):
    r"""Verify Theorem 3 (Full Trace = Hilbert Space Trace) for given r and β.

    Checks:
    (T3.1) Tr_{P(j)}(e^{-βH}) = dim P(j) · e^{-βh_j} for each j.
    (T3.2) The sum Σ dim P(j) · e^{-βh_j} equals Z_full.
    (T3.3) The Loewy decomposition gives the same result:
           Σ_j [2·dim L(j) + 2·dim L(r-2-j)] e^{-βh_j} = Σ_j dim P(j) · e^{-βh_j}
    (T3.4) The "naïve" simple-module sum differs from the correct trace.

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).
    beta : float
        Inverse temperature.

    Returns
    -------
    dict
        Verification results.
    """
    # (T3.1) Per-module thermal trace
    per_module = []
    for j in range(r):
        h_j = conformal_weight(j, r)
        dim_P = projective_module_dim(r, j)
        trace_j = dim_P * np.exp(-beta * h_j)
        per_module.append({
            'j': j,
            'h_j': h_j,
            'dim_P': dim_P,
            'thermal_trace_j': trace_j,
        })

    # (T3.2) Sum = Z_full
    Z_full_disc = sum(m['thermal_trace_j'] for m in per_module)
    Z_full_cont = full_thermal_trace_continuous(beta, r)
    Z_full_total = Z_full_disc + Z_full_cont

    # (T3.3) Loewy decomposition verification
    # Each dim P(j) = 2(j+1) + 2(r-1-j) for j < r-1
    loewy_sum = 0.0
    for j in range(r):
        h_j = conformal_weight(j, r)
        if j == r - 1:
            loewy_sum += r * np.exp(-beta * h_j)
        else:
            loewy_sum += (2 * (j + 1) + 2 * (r - 1 - j)) * np.exp(-beta * h_j)

    loewy_check = abs(loewy_sum - Z_full_disc) < 1e-10

    # (T3.4) Naïve simple-module sum
    naive_sum = 0.0
    for j in range(r):
        h_j = conformal_weight(j, r)
        naive_sum += (j + 1) * np.exp(-beta * h_j)  # Only counts L(j) once

    naive_differs = abs(naive_sum - Z_full_disc) > 1e-6

    # Detailed head/radical decomposition
    head_contribution = 0.0
    radical_contribution = 0.0
    for j in range(r):
        h_j = conformal_weight(j, r)
        if j == r - 1:
            head_contribution += r * np.exp(-beta * h_j)
        else:
            head_contribution += 2 * (j + 1) * np.exp(-beta * h_j)
            radical_contribution += 2 * (r - 1 - j) * np.exp(-beta * h_j)

    return {
        'r': r,
        'beta': beta,
        'Z_full_discrete': Z_full_disc,
        'Z_full_continuous': Z_full_cont,
        'Z_full_total': Z_full_total,
        'loewy_sum': loewy_sum,
        'loewy_check': loewy_check,
        'naive_simple_sum': naive_sum,
        'naive_differs_from_correct': naive_differs,
        'head_contribution': head_contribution,
        'radical_contribution': radical_contribution,
        'radical_fraction': radical_contribution / Z_full_disc if Z_full_disc > 0 else 0,
        'per_module': per_module,
        'theorem3_holds': loewy_check and naive_differs,
    }


