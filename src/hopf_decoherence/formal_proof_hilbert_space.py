# 9. ALTERNATING SUM IDENTITY (Key supporting lemma)
# ============================================================================

def verify_alternating_sum_identity(r):
    r"""Verify the key identity: Σ_{j=0}^{r-2} (-1)^j sin(π(j+1)/r) = 0.

    This identity is the root cause of destructive interference in Z_BCGP.

    PROOF: Let ω = -e^{iπ/r}. For odd r:
        ω^r = (-1)^r · e^{iπ} = (-1)·(-1) = 1
    so ω is an r-th root of unity. Then:

        Σ_{j=0}^{r-2} (-1)^j e^{iπ(j+1)/r}
        = e^{iπ/r} · Σ_{j=0}^{r-2} ω^j
        = e^{iπ/r} · (1 - ω^{r-1})/(1 - ω)
        = e^{iπ/r} · (1 - ω^{-1})/(1 - ω)     [since ω^r = 1]
        = e^{iπ/r} · (-ω^{-1})(1 - ω)/(1 - ω)  [since 1 - ω^{-1} = -ω^{-1}(1-ω)]
        = -e^{iπ/r} · ω^{-1}
        = -e^{iπ/r} · (-e^{-iπ/r})
        = 1

    Taking imaginary parts: Σ (-1)^j sin(π(j+1)/r) = Im(1) = 0. QED.

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    dict
        Verification results.
    """
    direct_sum = sum((-1) ** j * np.sin(np.pi * (j + 1) / r) for j in range(r - 1))
    full_sum = sum((-1) ** j * np.sin(np.pi * (j + 1) / r) for j in range(r))

    return {
        'r': r,
        'sum_j0_to_r2': direct_sum,
        'sum_j0_to_r1': full_sum,
        'identity_holds': abs(direct_sum) < 1e-10 and abs(full_sum) < 1e-10,
    }


