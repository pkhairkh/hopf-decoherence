# 3. THEOREM 2: DOUBLE COUNTING
# ============================================================================

def verify_theorem2(r):
    r"""Verify Theorem 2 (Double Counting) for given r.

    Checks:
    (T2.1) For each j < r-1: dim P(j) = 2(j+1) + 2(r-1-j) = 2r.
    (T2.2) For j = r-1: dim P(r-1) = r.
    (T2.3) Each L(k) for k < r-1 appears exactly twice in the projective
           modules (once in P(k) as head+socle, once in P(r-2-k) as radical).
    (T2.4) The total dimension equals Σ_k 4(k+1) + r (Steinberg).

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    dict
        Verification results including per-simple-module accounting.
    """
    # Check dimension identity for each P(j)
    dim_checks = []
    for j in range(r):
        dim_P = projective_module_dim(r, j)
        if j == r - 1:
            rhs = r
        else:
            rhs = 2 * (j + 1) + 2 * (r - 1 - j)
        dim_checks.append({
            'j': j,
            'dim_P': dim_P,
            'rhs': rhs,
            'check': dim_P == rhs,
        })

    # Track each simple L(k) across all projective modules
    simple_accounting = {}
    for k in range(r):
        simple_accounting[k] = {
            'k': k,
            'dim_L': k + 1,
            'appearances': [],  # List of (j, role, dimension)
            'total_dim_from_heads': 0,
            'total_dim_from_radicals': 0,
            'total_dim': 0,
        }

    for j in range(r):
        loewy = loewy_structure(r, j)
        for layer in loewy['layers']:
            label = layer['label']
            dim = layer['dim']
            role = layer['role']
            simple_accounting[label]['appearances'].append(
                (j, role, dim)
            )
            simple_accounting[label]['total_dim'] += dim
            if role in ('head', 'socle'):
                simple_accounting[label]['total_dim_from_heads'] += dim
            elif role in ('radical_1', 'radical_2'):
                simple_accounting[label]['total_dim_from_radicals'] += dim
            elif role == 'head=socle':
                # Steinberg: counted as head
                simple_accounting[label]['total_dim_from_heads'] += dim

    # Verify: each L(k) for k < r-1 appears in exactly 2 projective modules
    # (P(k) and P(r-2-k)), with total dimension 4(k+1)
    double_counting_checks = []
    for k in range(r):
        info = simple_accounting[k]
        if k < r - 1:
            expected_total_dim = 4 * (k + 1)
            expected_n_projectives = 2  # P(k) and P(r-2-k)
        else:
            expected_total_dim = r
            expected_n_projectives = 1  # P(r-1) only

        n_projectives = len(set(app[0] for app in info['appearances']))

        double_counting_checks.append({
            'k': k,
            'dim_L': k + 1,
            'total_dim': info['total_dim'],
            'expected_total_dim': expected_total_dim,
            'n_projectives': n_projectives,
            'expected_n_projectives': expected_n_projectives,
            'dim_check': info['total_dim'] == expected_total_dim,
            'counting_check': n_projectives == expected_n_projectives,
            'from_heads': info['total_dim_from_heads'],
            'from_radicals': info['total_dim_from_radicals'],
        })

    all_dim_ok = all(c['check'] for c in dim_checks)
    all_double_ok = all(
        c['dim_check'] and c['counting_check']
        for c in double_counting_checks
    )

    return {
        'r': r,
        'dim_identity_holds': all_dim_ok,
        'dim_checks': dim_checks,
        'double_counting_holds': all_double_ok,
        'double_counting_checks': double_counting_checks,
        'simple_accounting': simple_accounting,
        'theorem2_holds': all_dim_ok and all_double_ok,
    }


