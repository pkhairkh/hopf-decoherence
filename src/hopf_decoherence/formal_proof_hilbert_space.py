def compute_correction_factor(r, beta=1.0):
    r"""Compute the correction factor CF = Z_full_norm / Z_BCGP_norm.

    Both partition functions are normalized by D̃². The D̃² cancels in
    the ratio, so:

        CF = Z_full_unnorm / Z_BCGP_unnorm

    This should scale as √r × f(β) because:
        Z_full_norm ~ r^{-3/2}, Z_BCGP_norm ~ r^{-2}
        CF = r^{-3/2} / r^{-2} = r^{1/2} = √r

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).
    beta : float
        Inverse temperature.

    Returns
    -------
    dict
        Correction factor analysis.
    """
    Z_full_unnorm = full_thermal_trace(beta, r)
    Z_bcgp_unnorm = bcgp_partition_discrete(beta, r) + bcgp_partition_continuous(beta, r)

    # Both normalized by D̃² (D̃² cancels in ratio)
    D2 = modified_global_dimension(r)
    Z_full_norm = Z_full_unnorm / D2
    Z_bcgp_norm = Z_bcgp_unnorm / D2

    if abs(Z_bcgp_norm) < 1e-30:
        CF = float('inf')
        ln_CF = float('inf')
    else:
        CF = Z_full_norm / Z_bcgp_norm
        ln_CF = np.log(CF)

    # Compare with sqrt(r) * f(beta)
    sqrt_r = np.sqrt(r)
    f_beta = CF / sqrt_r if sqrt_r > 0 else float('inf')
    ln_CF_minus_half_ln_r = ln_CF - 0.5 * np.log(r)

    return {
        'r': r,
        'beta': beta,
        'Z_full_unnorm': Z_full_unnorm,
        'Z_bcgp_unnorm': Z_bcgp_unnorm,
        'D_tilde_squared': D2,
        'Z_full_norm': Z_full_norm,
        'Z_bcgp_norm': Z_bcgp_norm,
        'CF': CF,
        'ln_CF': ln_CF,
        'sqrt_r': sqrt_r,
        'f_beta': f_beta,
        'ln_CF_minus_half_ln_r': ln_CF_minus_half_ln_r,
        'correction_factor_scales_as_sqrt_r': abs(ln_CF_minus_half_ln_r) < 5.0,
    }


def extract_log_correction(partition_func, r_values, beta=1.0, **kwargs):
    """Extract the logarithmic entropy correction coefficient.

    Fits S(r) = a·ln(r) + b·r + c to extract the coefficient a.
    """
    r_odd = []
    entropies = []
    for r in r_values:
        if r % 2 == 0:
            continue
        try:
            dbeta = 1e-5
            Z = partition_func(beta, r, **kwargs)
            Z_plus = partition_func(beta + dbeta, r, **kwargs)
            Z_minus = partition_func(beta - dbeta, r, **kwargs)

            if abs(Z) < 1e-30:
                continue

            dlnZ = (Z_plus - Z_minus) / (2 * dbeta * Z)
            S = np.log(Z) + beta * dlnZ

            if np.isfinite(S):
                r_odd.append(r)
                entropies.append(S)
        except Exception:
            continue

    if len(r_odd) < 5:
        return {'log_coefficient': float('nan'), 'target': -1.5}

    r_vals = np.array(r_odd, dtype=float)
    S_vals = np.array(entropies)

    A = np.column_stack([np.log(r_vals), r_vals, np.ones_like(r_vals)])
    coeffs, _, _, _ = np.linalg.lstsq(A, S_vals, rcond=None)

    return {
        'log_coefficient': coeffs[0],
        'linear_coefficient': coeffs[1],
        'constant': coeffs[2],
        'target': -1.5,
        'deviation': abs(coeffs[0] - (-1.5)),
    }


def verify_corollary(r_values=None, beta=1.0):
    r"""Verify the Corollary by extracting the log correction for Z_full and Z_BCGP.

    Parameters
    ----------
    r_values : list of int, optional
        Odd r values. Default: 3, 5, 7, ..., 51.
    beta : float
        Inverse temperature.

    Returns
    -------
    dict
        Log correction comparison.
    """
    if r_values is None:
        r_values = list(range(3, 52, 2))

    # Extract log correction for Z_full (normalized by D̃²)
    def Z_full_norm(beta, r, **kwargs):
        D2 = modified_global_dimension(r)
        return full_thermal_trace(beta, r) / D2

    full_log = extract_log_correction(Z_full_norm, r_values, beta=beta)
    bcgp_log = extract_log_correction(bcgp_partition, r_values, beta=beta)

    # Extract correction factor scaling
    cf_results = [compute_correction_factor(r, beta) for r in r_values if r % 2 == 1]
    ln_CF_vals = [cf['ln_CF'] for cf in cf_results if np.isfinite(cf['ln_CF'])]
    r_vals_cf = [cf['r'] for cf in cf_results if np.isfinite(cf['ln_CF'])]

    if len(r_vals_cf) >= 5:
        r_arr = np.array(r_vals_cf, dtype=float)
        ln_CF_arr = np.array(ln_CF_vals)
        A = np.column_stack([np.log(r_arr), np.ones_like(r_arr)])
        cf_coeffs, _, _, _ = np.linalg.lstsq(A, ln_CF_arr, rcond=None)
        cf_log_coeff = cf_coeffs[0]
    else:
        cf_log_coeff = float('nan')

    return {
        'full_log_coefficient': full_log['log_coefficient'],
        'bcgp_log_coefficient': bcgp_log['log_coefficient'],
        'difference': full_log['log_coefficient'] - bcgp_log['log_coefficient'],
        'target_difference': 0.5,
        'cf_log_coefficient': cf_log_coeff,
        'target_cf_coeff': 0.5,
        'full_target': -1.5,
        'bcgp_target': -2.0,
    }


