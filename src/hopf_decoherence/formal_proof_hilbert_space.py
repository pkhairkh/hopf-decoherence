def verify_theorem1(r):
    r"""Verify Theorem 1 (Hilbert Space Decomposition) for given r.

    Checks:
    (T1.1) The Hilbert space has exactly r projective modules P(0),...,P(r-1).
    (T1.2) Each P(j) has the correct dimension.
    (T1.3) The total Hilbert space dimension (discrete sector) is:
           dim H_disc = Σ_j dim P(j) = (r-1)·2r + r = 2r² - r.
    (T1.4) The typical modules V_α for α ∈ (0,r)\ℤ have dim V_α = r.

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).

    Returns
    -------
    dict
        Verification results.
    """
    dims = [projective_module_dim(r, j) for j in range(r)]
    total_dim = sum(dims)
    expected_total = (r - 1) * 2 * r + r  # 2r² - r

    checks = {
        'r': r,
        'n_projective_modules': r,
        'dims_P': dims,
        'steinberg_dim': dims[-1],
        'steinberg_check': dims[-1] == r,
        'non_steinberg_dims': dims[:-1],
        'all_non_steinberg_2r': all(d == 2 * r for d in dims[:-1]),
        'total_dim': total_dim,
        'expected_total': expected_total,
        'total_dim_check': total_dim == expected_total,
        'typical_module_dim': r,
    }

    checks['theorem1_holds'] = (
        checks['steinberg_check']
        and checks['all_non_steinberg_2r']
        and checks['total_dim_check']
    )

    return checks


