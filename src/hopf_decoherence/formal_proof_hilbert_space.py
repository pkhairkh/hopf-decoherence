def simple_module_dim(j):
    """Dimension of the simple module L(j) for u_q(sl₂).

    Parameters
    ----------
    j : int
        Highest weight label, 0 ≤ j ≤ r-1.

    Returns
    -------
    int
        dim L(j) = j + 1.
    """
    return j + 1


def projective_module_dim(r, j):
    """Dimension of the projective indecomposable module P(j) for u_q(sl₂) at q = e^{2πi/r}.

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).
    j : int
        Label of P(j), 0 ≤ j ≤ r-1.

    Returns
    -------
    int
        dim P(j) = 2r for j < r-1, dim P(r-1) = r.
    """
    if j == r - 1:
        return r  # Steinberg
    return 2 * r


def conformal_weight(j, r):
    """Conformal weight h_j = j(j+2)/(4r) for L(j) in SU(2)_{r-2} WZW model.

    Parameters
    ----------
    j : int or float
        Spin label.
    r : int
        Root of unity parameter.

    Returns
    -------
    float
        Conformal weight h_j.
    """
    return j * (j + 2) / (4.0 * r)


def typical_conformal_weight(alpha, r):
    """Conformal weight of typical module V_α.

    Parameters
    ----------
    alpha : float
        Continuous label.
    r : int
        Root of unity parameter.

    Returns
    -------
    float
        h_α = (α² - 1)/(4r).
    """
    return (alpha ** 2 - 1) / (4.0 * r)


def loewy_structure(r, j):
    """Return the Loewy structure of P(j).

    For j < r-1:
        P(j): L(j) / L(r-2-j) / L(r-2-j) / L(j)
        Layer 0 (head):   L(j),      dim = j+1
        Layer 1 (rad¹):   L(r-2-j),  dim = r-1-j
        Layer 2 (rad²):   L(r-2-j),  dim = r-1-j
        Layer 3 (socle):  L(j),      dim = j+1

    For j = r-1 (Steinberg):
        P(r-1) = L(r-1), dim = r

    Parameters
    ----------
    r : int
        Root of unity parameter (odd ≥ 3).
    j : int
        Label of projective module P(j).

    Returns
    -------
    dict
        Loewy structure data.
    """
    h_j = conformal_weight(j, r)

    if j == r - 1:
        return {
            'j': j,
            'is_steinberg': True,
            'dim_P': r,
            'layers': [
                {'label': r - 1, 'dim': r, 'role': 'head=socle'}
            ],
            'composition_factors': {r - 1: 1},
            'head_simple': r - 1,
            'radical_simple': None,
            'conformal_weight': h_j,
        }

    dim_head = j + 1
    dim_rad = r - 1 - j
    k = r - 2 - j  # Radical simple label

    return {
        'j': j,
        'is_steinberg': False,
        'dim_P': 2 * r,
        'layers': [
            {'label': j, 'dim': dim_head, 'role': 'head'},
            {'label': k, 'dim': dim_rad, 'role': 'radical_1'},
            {'label': k, 'dim': dim_rad, 'role': 'radical_2'},
            {'label': j, 'dim': dim_head, 'role': 'socle'},
        ],
        'composition_factors': {j: 2, k: 2},
        'head_simple': j,
        'radical_simple': k,
        'conformal_weight': h_j,
    }


